import numpy as np

# ============================================================
# USER PARAMETERS
# ============================================================

outfile = 'ram.in'

freq = 100.0          # Hz
zs   = 50.0           # source depth
zr   = 50.0           # receiver depth

rmax = 20000.0        # meters
dr   = 100.0          # range step

zmax = 98.0           # water depth
dz   = 1.0            # depth step

c0 = 1500.0

# ============================================================
# SSP
# ============================================================

ssp = np.loadtxt('elba.ssp')

zssp = ssp[:,0]
cssp = ssp[:,1]

# ============================================================
# BATHYMETRY
# ============================================================

rbathy = [0.0, rmax]
zbathy = [98.0, 98.0]

# ============================================================
# BOTTOM SOUND SPEED
# ============================================================

zcb = [0.0]
cb  = [2000.0]

# ============================================================
# BOTTOM DENSITY
# ============================================================

zrho = [0.0]
rho  = [1.5]

# ============================================================
# ATTENUATION
# ============================================================

zattn = [0.0]
attn  = [0.5]

# ============================================================
# WRITE RAM INPUT FILE
# ============================================================

with open(outfile,'w') as f:

    # --------------------------------------------------------
    # TITLE
    # --------------------------------------------------------

    f.write('Python-generated RAM input\n')

    # --------------------------------------------------------
    # FREQ / SOURCE / RECEIVER
    # --------------------------------------------------------

    f.write(f'{freq:.1f} {zs:.1f} {zr:.1f}\n')

    # --------------------------------------------------------
    # RANGE GRID
    # --------------------------------------------------------

    f.write(f'{rmax:.1f} {dr:.1f} 1\n')

    # --------------------------------------------------------
    # DEPTH GRID
    # --------------------------------------------------------

    f.write(f'{zmax:.1f} {dz:.1f} 1 {zmax:.1f}\n')

    # --------------------------------------------------------
    # REFERENCE SOUND SPEED
    # --------------------------------------------------------

    f.write(f'{c0:.1f} 8 1 0.0\n')

    # --------------------------------------------------------
    # BATHYMETRY
    # --------------------------------------------------------

    for r,z in zip(rbathy,zbathy):

        f.write(f'{r:.1f} {z:.1f}\n')

    f.write('-1 -1\n')

    # --------------------------------------------------------
    # WATER COLUMN SSP
    # --------------------------------------------------------

    for z,c in zip(zssp,cssp):

        f.write(f'{z:.1f} {c:.3f}\n')

    f.write('-1 -1\n')

    # --------------------------------------------------------
    # BOTTOM SOUND SPEED
    # --------------------------------------------------------

    for z,c in zip(zcb,cb):

        f.write(f'{z:.1f} {c:.1f}\n')

    f.write('-1 -1\n')

    # --------------------------------------------------------
    # BOTTOM DENSITY
    # --------------------------------------------------------

    for z,r in zip(zrho,rho):

        f.write(f'{z:.1f} {r:.2f}\n')

    f.write('-1 -1\n')

    # --------------------------------------------------------
    # ATTENUATION
    # --------------------------------------------------------

    for z,a in zip(zattn,attn):

        f.write(f'{z:.1f} {a:.2f}\n')

    f.write('-1 -1\n')

print(f'Wrote {outfile}')
